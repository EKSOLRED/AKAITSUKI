create trigger titles_touch_updated_at
before update on public.titles
for each row execute function public.touch_updated_at();

insert into public.site_content (key, payload)
values
  ('titles', '[]'::jsonb),
  ('about', '{}'::jsonb)
on conflict (key) do nothing;

do $$
declare
  legacy_titles jsonb;
begin
  if exists (select 1 from public.site_content where key = 'titles')
    and not exists (select 1 from public.titles limit 1) then
    select payload into legacy_titles
    from public.site_content
    where key = 'titles';

    if jsonb_typeof(legacy_titles) = 'array' and jsonb_array_length(legacy_titles) > 0 then
      perform public.import_legacy_titles_payload(legacy_titles);
    end if;
  end if;
end;
$$;
