create or replace function public.import_legacy_titles_payload(payload jsonb)
returns void
language plpgsql
as $$
declare
  title_item jsonb;
  season_item jsonb;
  episode_item jsonb;
  voiceover_item jsonb;
  player_item jsonb;
  title_id bigint;
  season_id text;
  episode_id text;
  voiceover_id text;
  title_index integer;
  season_index integer;
  episode_index integer;
  voiceover_index integer;
  player_index integer;
begin
  if jsonb_typeof(payload) is distinct from 'array' then
    return;
  end if;

  delete from public.titles;

  title_index := 0;
  for title_item in select value from jsonb_array_elements(payload) loop
    title_index := title_index + 1;
    title_id := coalesce(nullif(title_item ->> 'id', '')::bigint, title_index::bigint);

    insert into public.titles (
      id,
      title,
      type,
      year,
      season,
      status,
      rating,
      duration,
      team,
      voiceover,
      description,
      long_text,
      poster_theme,
      poster_url,
      poster_gradient,
      video_url,
      channel,
      release_date,
      linked_title_id,
      latest_episode,
      updated_order,
      popularity
    )
    values (
      title_id,
      coalesce(title_item ->> 'title', ''),
      coalesce(title_item ->> 'type', 'anime'),
      coalesce(nullif(title_item ->> 'year', '')::integer, extract(year from now())::integer),
      coalesce(title_item ->> 'season', ''),
      coalesce(title_item ->> 'status', ''),
      coalesce(nullif(title_item ->> 'rating', '')::numeric, 0),
      coalesce(title_item ->> 'duration', ''),
      coalesce(title_item ->> 'team', ''),
      coalesce(title_item ->> 'voiceover', title_item ->> 'team', ''),
      coalesce(title_item ->> 'description', ''),
      coalesce(title_item ->> 'longText', ''),
      coalesce(title_item ->> 'posterTheme', 'electric-violet'),
      coalesce(title_item ->> 'posterUrl', ''),
      coalesce(title_item ->> 'posterGradient', title_item ->> 'poster', ''),
      coalesce(title_item ->> 'videoUrl', ''),
      coalesce(title_item ->> 'channel', ''),
      coalesce(title_item ->> 'releaseDate', ''),
      nullif(title_item ->> 'linkedTitleId', '')::bigint,
      coalesce(title_item ->> 'latestEpisode', ''),
      coalesce(nullif(title_item ->> 'updatedOrder', '')::integer, 2147483647),
      coalesce(nullif(title_item ->> 'popularity', '')::numeric, nullif(title_item ->> 'rating', '')::numeric, 0)
    )
    on conflict (id) do update
    set
      title = excluded.title,
      type = excluded.type,
      year = excluded.year,
      season = excluded.season,
      status = excluded.status,
      rating = excluded.rating,
      duration = excluded.duration,
      team = excluded.team,
      voiceover = excluded.voiceover,
      description = excluded.description,
      long_text = excluded.long_text,
      poster_theme = excluded.poster_theme,
      poster_url = excluded.poster_url,
      poster_gradient = excluded.poster_gradient,
      video_url = excluded.video_url,
      channel = excluded.channel,
      release_date = excluded.release_date,
      linked_title_id = excluded.linked_title_id,
      latest_episode = excluded.latest_episode,
      updated_order = excluded.updated_order,
      popularity = excluded.popularity,
      updated_at = now();

    insert into public.title_alt_titles (title_id, position, value)
    select title_id, ord::integer, trim(value)
    from jsonb_array_elements_text(coalesce(title_item -> 'altTitles', '[]'::jsonb)) with ordinality as t(value, ord)
    where trim(value) <> '';

    insert into public.title_genres (title_id, position, value)
    select title_id, ord::integer, trim(value)
    from jsonb_array_elements_text(coalesce(title_item -> 'genres', '[]'::jsonb)) with ordinality as t(value, ord)
    where trim(value) <> '';

    insert into public.title_categories (title_id, position, value)
    select title_id, ord::integer, trim(value)
    from jsonb_array_elements_text(coalesce(title_item -> 'categories', '[]'::jsonb)) with ordinality as t(value, ord)
    where trim(value) <> '';

    if jsonb_typeof(title_item #> '{media,seasons}') = 'array' then
      season_index := 0;
      for season_item in select value from jsonb_array_elements(title_item #> '{media,seasons}') loop
        season_index := season_index + 1;
        season_id := coalesce(nullif(season_item ->> 'id', ''), gen_random_uuid()::text);

        insert into public.title_seasons (id, title_id, number, position)
        values (
          season_id,
          title_id,
          coalesce(nullif(season_item ->> 'number', '')::integer, season_index),
          season_index
        );

        if jsonb_typeof(season_item -> 'episodes') = 'array' then
          episode_index := 0;
          for episode_item in select value from jsonb_array_elements(season_item -> 'episodes') loop
            episode_index := episode_index + 1;
            episode_id := coalesce(nullif(episode_item ->> 'id', ''), gen_random_uuid()::text);

            insert into public.title_episodes (id, title_id, season_id, number, title, is_deleted, position)
            values (
              episode_id,
              title_id,
              season_id,
              coalesce(nullif(episode_item ->> 'number', '')::integer, episode_index),
              coalesce(episode_item ->> 'title', ''),
              coalesce((episode_item ->> 'isDeleted')::boolean, false),
              episode_index
            );

            if jsonb_typeof(episode_item -> 'voiceovers') = 'array' then
              voiceover_index := 0;
              for voiceover_item in select value from jsonb_array_elements(episode_item -> 'voiceovers') loop
                voiceover_index := voiceover_index + 1;
                voiceover_id := coalesce(nullif(voiceover_item ->> 'id', ''), gen_random_uuid()::text);

                insert into public.episode_voiceovers (id, title_id, episode_id, name, position)
                values (
                  voiceover_id,
                  title_id,
                  episode_id,
                  coalesce(nullif(trim(voiceover_item ->> 'name'), ''), format('Озвучка %s', voiceover_index)),
                  voiceover_index
                );

                if jsonb_typeof(voiceover_item -> 'players') = 'array' then
                  player_index := 0;
                  for player_item in select value from jsonb_array_elements(voiceover_item -> 'players') loop
                    player_index := player_index + 1;

                    insert into public.voiceover_players (id, title_id, voiceover_id, name, src, position)
                    values (
                      coalesce(nullif(player_item ->> 'id', ''), gen_random_uuid()::text),
                      title_id,
                      voiceover_id,
                      coalesce(nullif(trim(player_item ->> 'name'), ''), format('Плеер %s', player_index)),
                      coalesce(player_item ->> 'src', player_item ->> 'iframe', ''),
                      player_index
                    );
                  end loop;
                end if;
              end loop;
            end if;
          end loop;
        end if;
      end loop;
    end if;
  end loop;
end;
$$;

